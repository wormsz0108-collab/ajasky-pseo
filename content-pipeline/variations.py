def pick_variation(board_title: str) -> dict:
    """매 발행마다 호출. 랜덤 voice + section titles + title pattern + length profile + FAQ."""
    voice = random.choice(VOICES)
    section_titles = {
        key: random.choice(pool)
        for key, pool in SECTION_TITLES.items()
    }
    title_pattern = random.choice(TITLE_PATTERNS)
    length_profile = random.choice(BODY_LENGTH_PROFILES)
    faq_pool = FAQ_POOL.get(board_title, FAQ_POOL["스카이차"])
    faq_samples = random.sample(faq_pool, k=min(4, len(faq_pool)))
    return {
        "voice": voice,
        "section_titles": section_titles,
        "title_pattern": title_pattern,
        "length_profile": length_profile,
        "faq_samples": faq_samples,
    }
