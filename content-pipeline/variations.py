# ----------------------------------------------------------------------------
# 통합 변동 선택기
# ----------------------------------------------------------------------------
def pick_variation(seed: str | None = None) -> dict[str, Any]:
    """슬러그를 시드로 하거나 완전 랜덤하게 변동 세트 선택.

    같은 slug 재발행 시 같은 변동 (재현성)을 원하면 seed를 줘도 됨.
    기본은 매번 다른 결과.
    """
    rng = random.Random(seed) if seed else random.Random()

    section_aliases = {k: rng.choice(v) for k, v in SECTION_ALIAS_POOLS.items()}

    return {
        "voice": rng.choice(VOICES),
        "title_format": rng.choice(TITLE_FORMATS),
        "section_aliases": section_aliases,
        "intro_style": rng.choice(INTRO_STYLES),
        "body_length": rng.choice(BODY_LENGTH_TARGETS),
        "list_style": rng.choice(LIST_STYLES),
        "closing_style": rng.choice(CLOSING_STYLES),
    }


def format_for_prompt(var: dict[str, Any]) -> dict[str, str]:
    """variations dict → prompt-template.txt 안 {placeholder} 들 채울 값들로 변환."""
    aliases = var["section_aliases"]
    body_len = var["body_length"]
    return {
        "voice_name": var["voice"]["name"],
        "voice_instruction": var["voice"]["instruction"],
        "alias_select": aliases["선택"],
        "alias_check": aliases["현장 체크"],
        "alias_process": aliases["진행 절차"],
        "alias_cost": aliases["비용 기준"],
        "alias_caution": aliases["주의할 점"],
        "alias_consult": aliases["상담 팁"],
        "alias_faq": aliases["FAQ"],
        "alias_area": aliases["서비스 지역"],
        "intro_style": var["intro_style"],
        "h2_min": str(body_len["h2_chars"][0]),
        "h2_max": str(body_len["h2_chars"][1]),
        "list_style": var["list_style"],
        "closing_style": var["closing_style"],
    }
